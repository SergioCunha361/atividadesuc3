// Contexto: Ricardo quer verificar o rendimento de seus veículos.
// Questão: Se o rendimento for menor que 10 km/l, imprimir "Baixo rendimento". Se for entre 10
// e 15 km/l, imprimir "Rendimento médio". Se for maior que 15 km/l, imprimir "Alto rendimento".


let rendVeiculo = parseFloat(prompt("Digite a o consumo do Veículo: "));
if (rendVeiculo > 15) {
    alert(`O consumo de ${rendVeiculo} km/l está ótimo - Alto Rendimento.`);
}else if (rendVeiculo >=10 && rendVeiculo <=15) {
    alert(`O consumo de ${rendVeiculo} km/l está médio - Rendimento Médio.`);
}else {
    alert(`O consumo de ${rendVeiculo} km/l está baixo - Baixo Rendimento.`);
}


